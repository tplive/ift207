/** An interface for methods that return
 * the perimeter and are of an object
 */

/**
 * @author Thomas
 *
 */
public interface Measurable {
	/** Gets the perimeter.
	 * @return The perimeter. 
	 */
	public double getPerimeter();
	
	/** Gets the area.
	 * @return the area.
	 */
	public double getArea();
}// end Measurable
